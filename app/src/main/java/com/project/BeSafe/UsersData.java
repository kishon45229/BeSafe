package com.project.BeSafe;

public class UsersData {
    String firstName, lastName, dob, nicNo, mobileNo, email;

    public UsersData() {
    }

    public UsersData(String firstName, String lastName, String dob, String nicNo, String mobileNo, String email) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.dob = dob;
        this.nicNo = nicNo;
        this.mobileNo = mobileNo;
        this.email = email;
    }
}
